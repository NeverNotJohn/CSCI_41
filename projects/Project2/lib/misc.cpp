void print(vector<int> lector) {
    cout << left;
    for (int i = 0; i < 100; i++)
    {
        cout << setw(4) << lector[i];
        int j = i + 1;
        if (j % 10 == 0) { cout << endl;}
    }
}

void print(vector<item> lector) {
    cout << left;
    for (int i = 0; i < 100; i++)
    {
        cout << setw(4) << lector[i].priority;
        int j = i + 1;
        if (j % 10 == 0) { cout << endl;}
    }
}

void swap(int i, int j, vector<int> &mocter) {
    int temp = mocter[i];
    mocter[i] = mocter[j];
    mocter[j] = temp;
}

void swap(int i, int j, vector<item> &mocter) {
    item temp = mocter[i];
    mocter[i] = mocter[j];
    mocter[j] = temp;
}